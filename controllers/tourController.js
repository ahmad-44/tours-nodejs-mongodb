exports.getAllTours = async (req, res) => {
  try {
    console.log(req.query);
    // BUILD QUERY
    // 1) Filtering
    const queryObj = { ...req.query };
    const excludedFields = ['page', 'sort', 'limit', 'fields'];
    excludedFields.forEach(el => delete queryObj[el]);

    let query = Tour.find(queryObj); // localhost:3000/api/v1/tours?duration[$gte]=5&difficulty=easy

    // 2) Sorting
    if (req.query.sort) {
      query = query.sort(req.query.sort); //localhost:3000/api/v1/tours?sort=-price+duration
    } else {
      query = query.sort('_id');
    }

    // 3) Field Limiting
    if (req.query.fields) {
      query = query.select(req.query.fields);
    } else {
      query = query.select('-__v'); // __v is a field we want to remove
    }

    //4) Pagination
    // set default to be 100 items per page if pagination filters are not applied
    const page = +req.query.page * 1 || 1;
    const limit = +req.query.limit * 1 || 100;
    const skip = (page - 1) * limit;

    //page=3&limit=10, 1-10, page 1, 11-20 page2, 21-30 page3
    query = query.skip(skip).limit(limit);

    // if page exceeds the limit
    if (req.query.page) {
      const numTours = await Tour.countDocuments();
      if (skip >= numTours) throw new Error('This page does not exist');
    }

    // EXECUTE QUERY
    const tours = await query;

    // one more way of quering
    // const query = Tour.find()
    //   .where('duration')
    //   .equals(5)
    //   .where('difficulty')
    //   .equals('easy');
    // SEND RESPONSE
    res.status(200).json({
      status: 'success',
      results: tours.length,
      data: {
        tours
      }
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err.message
    });
  }
};

exports.getTour = async (req, res) => {
  try {
    const tour = await Tour.findById(req.params.id);
    res.status(200).json({
      status: 'success',
      data: {
        tour
      }
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err.message
    });
  }
};

exports.createTour = async (req, res) => {
  // const newTour = new Tour({});
  // newTpur.save();

  try {
    const newTour = await Tour.create(req.body);

    res.status(201).json({
      status: 'success',
      data: {
        tour: newTour
      }
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err
    });
  }
};

exports.updateTour = async (req, res) => {
  try {
    const tour = await Tour.findByIdAndUpdate(req.params.id, req.body, {
      new: true,
      runValidators: true
    });
    res.status(200).json({
      status: 'success',
      data: {
        tour
      }
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err.message
    });
  }
};

exports.deleteTour = async (req, res) => {
  try {
    await Tour.findByIdAndDelete(req.params.id);
    res.status(200).json({
      status: 'success',
      data: null
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err.message
    });
  }
};
